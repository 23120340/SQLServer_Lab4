   File 01: Create database and tables
   ========================================================= */
USE master;
GO

IF DB_ID('QLSVNhom') IS NOT NULL
BEGIN
    ALTER DATABASE QLSVNhom SET SINGLE_USER WITH ROLLBACK IMMEDIATE;
    DROP DATABASE QLSVNhom;
END
GO

CREATE DATABASE QLSVNhom;
GO

USE QLSVNhom;
GO

CREATE TABLE NHANVIEN (
    MANV        VARCHAR(20)     NOT NULL PRIMARY KEY,
    HOTEN       NVARCHAR(100)   NOT NULL,
    EMAIL       VARCHAR(100)    NULL,
    LUONG       VARBINARY(MAX)  NULL,      -- RSA ciphertext encrypted by employee's own public key
    LUONG_ADMIN VARBINARY(MAX)  NULL,      -- RSA ciphertext encrypted by ADMIN's public key (so admin can always decrypt)
    TENDN       NVARCHAR(100)   NOT NULL UNIQUE,
    MATKHAU     VARBINARY(20)   NOT NULL,  -- SHA1 hash generated by client
    PUBKEY      NVARCHAR(MAX)   NOT NULL,  -- RSA 2048 public key generated by client
    VAITRO      VARCHAR(20)     NOT NULL CONSTRAINT DF_NHANVIEN_VAITRO DEFAULT ('NHANVIEN'),
    CONSTRAINT CK_NHANVIEN_VAITRO CHECK (VAITRO IN ('ADMIN', 'NHANVIEN'))
);
GO

CREATE TABLE HOCPHAN (
    MAHP    VARCHAR(20)     NOT NULL PRIMARY KEY,
    TENHP   NVARCHAR(100)   NOT NULL,
    SOTC    INT             NULL
);
GO

CREATE TABLE LOP (
    MALOP   VARCHAR(20)     NOT NULL PRIMARY KEY,
    TENLOP  NVARCHAR(100)   NOT NULL,
    MANV    VARCHAR(20)     NULL,
    MASV    NVARCHAR(20)    NULL,
    MAHP    VARCHAR(20)     NULL,
    CONSTRAINT FK_LOP_NV FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
    CONSTRAINT FK_LOP_HP FOREIGN KEY (MAHP) REFERENCES HOCPHAN(MAHP)
);
GO

CREATE TABLE SINHVIEN (
    MASV        NVARCHAR(20)    NOT NULL PRIMARY KEY,
    HOTEN       NVARCHAR(100)   NOT NULL,
    NGAYSINH    DATE            NULL,
    DIACHI      NVARCHAR(200)   NULL,
    MALOP       VARCHAR(20)     NOT NULL,
    TENDN       NVARCHAR(100)   NOT NULL UNIQUE,
    MATKHAU     VARBINARY(20)   NOT NULL,
    CONSTRAINT FK_SV_LOP FOREIGN KEY (MALOP) REFERENCES LOP(MALOP)
);
GO

ALTER TABLE LOP
    ADD CONSTRAINT FK_LOP_SV FOREIGN KEY (MASV) REFERENCES SINHVIEN(MASV);
GO

CREATE TABLE BANGDIEM (
    MASV        NVARCHAR(20)    NOT NULL,
    MAHP        VARCHAR(20)     NOT NULL,
    DIEMTHI     VARBINARY(MAX)  NULL,      -- RSA ciphertext generated by client
    MANV_NHAP   VARCHAR(20)     NOT NULL,  -- employee whose public key encrypted DIEMTHI
    CONSTRAINT PK_BANGDIEM PRIMARY KEY (MASV, MAHP),
    CONSTRAINT FK_BD_SV FOREIGN KEY (MASV) REFERENCES SINHVIEN(MASV),
    CONSTRAINT FK_BD_HP FOREIGN KEY (MAHP) REFERENCES HOCPHAN(MAHP),
    CONSTRAINT FK_BD_NV FOREIGN KEY (MANV_NHAP) REFERENCES NHANVIEN(MANV)
);
GO

PRINT N'Created database QLSVNhom for Lab04.';
GO
