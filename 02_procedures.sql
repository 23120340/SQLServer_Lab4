CREATE PROCEDURE SP_INS_HOCPHAN
    @MAHP VARCHAR(20),
    @TENHP NVARCHAR(100),
    @SOTC INT
AS
BEGIN
    SET NOCOUNT ON;

    IF EXISTS (SELECT 1 FROM HOCPHAN WHERE MAHP = @MAHP)
        UPDATE HOCPHAN SET TENHP = @TENHP, SOTC = @SOTC WHERE MAHP = @MAHP;
